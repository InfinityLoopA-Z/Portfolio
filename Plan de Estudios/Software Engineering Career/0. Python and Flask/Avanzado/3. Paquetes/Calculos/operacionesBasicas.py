def suma(num1, num2):
    print("El resultado es:",num1+num2)

def resta(num1, num2):
    print("El resultado es:",num1-num2)

def multiplicacion(num1, num2):
    print("El resultado es:",num1*num2)

def division(num1, num2):
    print("El resultado es:",num1/num2)

def potencia(base, exponente):
    print("El resultado es:",base **exponente)

def redondeo(num):
    print("El resultado es:",round(num))